#include<stdio.h>
int main()
{
    int language=10;
    switch(language)
    {
    case 1:
        printf("c#\n");
        break;
    case 2:
        printf("c\n");
        break;
    case 3:
        printf("c++\n");
        break;
    default:
        printf("other programming language\n");
        break;
    }

    return 0;

}
